package org.ksrntheja.javaprograms.old;

public class PrintSeparator {

    public static void separator(int number) {
        System.out.println("====================================================================================");
        System.out.println(number);
        System.out.println("====================================================================================");
    }

    public static void separator(String string) {
        System.out.println("====================================================================================");
        System.out.println(string);
        System.out.println("====================================================================================");
    }

}
